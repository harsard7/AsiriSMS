    private void btnfinalpaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfinalpaymentActionPerformed
        if (combobx1custid.getSelectedIndex() == -1) {
            return;
        }

        OrdersDTO order = new OrdersDTO(txtOrdId.getText(),
                combobx1custid.getSelectedItem().toString(),
                employeecombo.getSelectedItem().toString(),
                txtdate.getText());
         Date date = null;
        try {
            date = new SimpleDateFormat("yyyy-MM-dd").parse(txtdate.getText());
        } catch (ParseException ex) {
            Logger.getLogger(ManageOrder.class.getName()).log(Level.SEVERE, null, ex);
        }
        ArrayList<OrderDetailDTO> alOrderDetails = new ArrayList<>();
         
        DefaultTableModel dtm = (DefaultTableModel) tblorder.getModel();
        for (int i = 0; i < dtm.getRowCount(); i++) {

            OrderDetailDTO orderDetail = new OrderDetailDTO(order.getOID(), dtm.getValueAt(i, 0).toString(),
                    Integer.parseInt(dtm.getValueAt(i, 4).toString()),
                    Double.parseDouble(dtm.getValueAt(i, 3).toString()));
            alOrderDetails.add(orderDetail);
        }
        try {
            boolean result = ManageOrdersController.ManageOrdersController(order, alOrderDetails);
              
            if (result) {
                   
                JOptionPane.showMessageDialog(this, "Order has been successfully placed");
                //setId();
                 PaymentFinal mn=new PaymentFinal();
        
                mn.lbloid.setText(txtOrdId.getText());
                mn.lblorddate.setText(txtdate.getText());
                mn.lblamount.setText(txtamount.getText());
                mn.lbldiscount.setText(txtdiscount.getText());
                mn.lbltotamount.setText(txttotalamount.getText());
                mn.lblcusid.setText(combobx1custid.getSelectedItem().toString());
                mn.lblempid.setText(employeecombo.getSelectedItem().toString());
                mn.setSize(panel1.getSize());
                panel1.removeAll();
                panel1.add(mn);
                panel1.repaint();
                panel1.revalidate();
                mn.txtpaidamount.setText(txttotalamount.getText());
                mn.txtpaidamount.selectAll();
                mn.txtpaidamount.requestFocus();
               // setInvId();
                clearfields();
            } else {
                  
                JOptionPane.showMessageDialog(this, "Order hasn't been placed ");
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageOrder.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnfinalpaymentActionPerformed

