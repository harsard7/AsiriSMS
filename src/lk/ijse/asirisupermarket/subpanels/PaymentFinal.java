    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
       
            PaymentDTO payment=new PaymentDTO(lblInvoice.getText(), lbloid.getText(),
                    lblcusid.getText(),
                    lblempid.getText(),lblorddate.getText(),
                    Double.parseDouble(lbldiscount.getText()),Double.parseDouble(lbltotamount.getText()));
            
             try {
            boolean status=ManagePaymentController.paymentDAO.add(payment);
            if (status) {
            JOptionPane.showMessageDialog(this,"Payment Has been Succesfully saved");
            new MainWindow().setVisible(true);
            this.setVisible(false);
            clearfields();
            }else{
             JOptionPane.showMessageDialog(this,"Payment Has Not  been  saved");
            }
        } catch (Exception ex) {
            Logger.getLogger(PaymentFinal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnsaveActionPerformed

