    private void txtpaidamountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtpaidamountActionPerformed
        double paidamt=Double.parseDouble(txtpaidamount.getText());
        double totalamt=Double.parseDouble(lbltotamount.getText());
        double balanceamt=paidamt-totalamt;
        if(paidamt<totalamt){
            JOptionPane.showMessageDialog(this, "Not Sufficeient Money");
        }else if
            (balanceamt>5000){
            JOptionPane.showMessageDialog(this, "Change above the allowed limit");
        }else{
        lblchange.setText(balanceamt+"0");
        }
        

    }//GEN-LAST:event_txtpaidamountActionPerformed

