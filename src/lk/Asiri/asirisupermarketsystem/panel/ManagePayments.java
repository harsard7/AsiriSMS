    private void loadallpaymentIds() {
       try {
            ArrayList<PaymentDTO> allPaymentid = ManagePaymentController.paymentDAO.getAll();
           DefaultTableModel dtm=(DefaultTableModel) tblpayment.getModel();
            comboinvid.removeAllItems();
            if (allPaymentid == null) {
                return;
            }

            for (PaymentDTO payment : allPaymentid) {
                comboinvid.addItem(payment.getPID());
            }
            dtm.setRowCount(0);
            if (allPaymentid !=null){
            for(PaymentDTO payment :allPaymentid){
            Object[] rowData={
            payment.getPID(),
            payment.getPayment_DATE(),
            payment.getCID(),
            payment.getOID(),
            payment.getEID(),
            payment.getDiscount(),
            payment.getTotal_Amount()
            };
            dtm.addRow(rowData);
         }
            }
        } catch (Exception ex) {
            Logger.getLogger(ManagePayments.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
