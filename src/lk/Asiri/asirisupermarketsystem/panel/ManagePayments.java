    private void comboinvidItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboinvidItemStateChanged
        try {
            if(comboinvid.getSelectedIndex()==-1){
                return;
            }
             PaymentDTO payment=ManagePaymentController.paymentDAO.search(new PaymentDTO(comboinvid.getSelectedItem().toString(), null,null,null,null,0.0,0.0));
            if (payment == null) {
                return;
            }

            txtinvdate.setText(payment.getPayment_DATE());
            txtcid.setText(payment.getCID());
            txtempid.setText(payment.getEID());
            txtoid.setText(payment.getOID());
            txtdiscount.setText(payment.getDiscount()+"");
            txttotamount.setText(payment.getTotal_Amount()+"");
        } catch (Exception ex) {
            Logger.getLogger(ManagePayments.class.getName()).log(Level.SEVERE, null, ex);
        }
        
       
    }//GEN-LAST:event_comboinvidItemStateChanged

