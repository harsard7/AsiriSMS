    private void tblpaymentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblpaymentMouseClicked
       
        if (tblpayment.getSelectedRow()==-1)return;
        
        String pid=tblpayment.getValueAt(tblpayment.getSelectedRow(), 0).toString();
        String orderdate=tblpayment.getValueAt(tblpayment.getSelectedRow(), 1).toString();
        String cid=tblpayment.getValueAt(tblpayment.getSelectedRow(), 2).toString();
        String oid=tblpayment.getValueAt(tblpayment.getSelectedRow(), 3).toString();
        String eid=tblpayment.getValueAt(tblpayment.getSelectedRow(),4).toString();
        String discount=tblpayment.getValueAt(tblpayment.getSelectedRow(), 5).toString();
        String totamount=tblpayment.getValueAt(tblpayment.getSelectedRow(),6).toString();
      
       
        
        comboinvid.setSelectedItem(pid);
        txtinvdate.setText(orderdate);
        txtempid.setText(eid);
        txtdiscount.setText(discount);
        txttotamount.setText(totamount);
        txtoid.setText(oid);
        txtcid.setText(cid);
        
    }//GEN-LAST:event_tblpaymentMouseClicked

