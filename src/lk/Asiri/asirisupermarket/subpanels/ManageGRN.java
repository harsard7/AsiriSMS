    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        SimpleDateFormat dat=new SimpleDateFormat("yyyy-MM-dd");
        String date=dat.format(datePickergrn.getDate());
        GrnDTO grn=new GrnDTO(
         txtGrnid.getText(),
        date,
         combosid.getSelectedItem().toString(),
         new BigDecimal(txttotamount.getText()));
        
       
       

        try {

            boolean result = ManageGRNController.grnDAO.update(grn);
                //updateBrand(brand);
            if (result) {
                JOptionPane.showMessageDialog(this, "GRN has been successfully updated");
               // btn_refreshActionPerformed(evt);
               loadAllGrns(); 
                clearAllTextFileds();
            } else {
                JOptionPane.showMessageDialog(this, "GRN hasn't been updated");
            }

        } catch (Exception ex) {
            Logger.getLogger(ManageGRN.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updatebtnActionPerformed

