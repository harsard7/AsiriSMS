    private void loadAllGrns() {
        
           
        try {
            ArrayList<GrnDTO> allGrns = ManageGRNController.grnDAO.getAll();
            
            DefaultTableModel dtm = (DefaultTableModel) tblGrn.getModel();
            
            dtm.setRowCount(0);
            
            if (allGrns != null){
                
                for (GrnDTO grn : allGrns) {
                    
                    Object[] rowData = {
                        grn.getGRNID(),
                        grn.getGRN_Date(),
                        grn.getSID(),
                        grn.getTotalAmount()
                        
                    };
                    
                    dtm.addRow(rowData);
                    
                }
                
            }   } catch (Exception ex) {
            Logger.getLogger(ManageGRN.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

