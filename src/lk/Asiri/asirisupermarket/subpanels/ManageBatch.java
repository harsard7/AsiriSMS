    private void loadallgrnids() {
        try {
            combogrnid.removeAllItems();
            ArrayList<GrnDTO>alghrnid=ManageGRNController.grnDAO.getAll();
            if(alghrnid==null){return;}
            
            for(GrnDTO grn:alghrnid){
                combogrnid.addItem(grn.getGRNID());
            }
        } catch (Exception ex) {
            Logger.getLogger(ManageBatch.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

