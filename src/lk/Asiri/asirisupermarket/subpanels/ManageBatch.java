    private void loadAllBatches() {
        
           
        try {
            ArrayList<BatchDTO> allBatchs = ManageBatchController.batchDAO.getAll();
            
            DefaultTableModel dtm = (DefaultTableModel) tblbatch.getModel();
            
            dtm.setRowCount(0);
            
            if (allBatchs != null){
                
                for (BatchDTO batch : allBatchs) {
                    
                    Object[] rowData = {
                        batch.getBID(),
                        batch.getGRNID(),
                        batch.getSID(),
                        batch.getDate()
                    };
                    
                    dtm.addRow(rowData);
                    
                }
                
            }   } catch (Exception ex) {
            Logger.getLogger(ManageBatch.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

